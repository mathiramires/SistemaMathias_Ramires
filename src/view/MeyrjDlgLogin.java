    private void meyrjBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_meyrjBtnConfirmarActionPerformed
        // TODO add your handling code here:

        String usuario = meyrjTxtUsuario.getText();
        String senha = new String(meyrjPwdSenha.getPassword());

        MeyrUsuariosDAO usuarioDao = new MeyrUsuariosDAO();
        boolean temLogin = usuarioDao.listaDoLogin(usuario, senha);

        if (temLogin == true) {
            MeyrJFrmPrincipal principal = new MeyrJFrmPrincipal();
            principal.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Usu√°rio ou senha incorretos");
            tentativas--;
            if(tentativas==0){
                System.exit(0);
            }
        }
    }//GEN-LAST:event_meyrjBtnConfirmarActionPerformed

